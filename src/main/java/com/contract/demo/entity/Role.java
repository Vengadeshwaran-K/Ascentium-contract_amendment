package com.contract.demo.entity;

import jakarta.persistence.*;
import lombok.Data;

@Entity
@Table(name = "roles")
@Data
public class Role {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(unique = true, nullable = false)
    private String name; // SUPER_ADMIN, ADMIN, LEGAL_USER, FINANCE_REVIEWER, CLIENT
}
